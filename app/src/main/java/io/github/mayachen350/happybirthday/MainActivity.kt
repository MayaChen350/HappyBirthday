                }
            }
        }
    }
}

@Composable
fun GreetingText(message: String, from: String, modifier: Modifier = Modifier) {
    Column (
        verticalArrangement = Arrangement.Center,
        modifier = modifier
    ) {
        Text(
            textAlign = TextAlign.Center,
            text = message,
            fontSize = 90.sp,
            lineHeight = 100.sp
        )

        Text(
            text = from,
            fontSize = 36.sp,
            modifier = Modifier.align(alignment = Alignment.CenterHorizontally)
        )
    }
}

